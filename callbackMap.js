var words = ["ground", "control", "to", "major", "tom"];

map(words, function(word) {
  return word.length;
});

map(words, function(word) {
  return word.toUpperCase();
});

map(words, function(word) {
  return word.split('').reverse().join('');
});

function map(arr, func){
  var result = [];
  for (var arr1 of arr){
    result.push(func(arr1));
  }
  console.log(result);
}
